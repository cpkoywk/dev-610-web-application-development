public class e2q4 {
    public static void main(String[] args) {
        int n = 4; //how many rows to print

        for (int i = 0; i < n; i++) {
            // print '*' 
            for (int j = 0; j < 5; j++) { //how many * to print for each row
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
